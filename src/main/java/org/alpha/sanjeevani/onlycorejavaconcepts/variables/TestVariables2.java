package org.alpha.sanjeevani.onlycorejavaconcepts.variables;

public class TestVariables2 {
    public static void main(String[] args) {
         byte b=-127;
         short s=-500;
         int i=254;
         long l=567;
         float f=56.67f;
         double d=345.678;
         boolean bb=true;
         char ch='S';
        System.out.println(" [value of byte] :" +b);
        System.out.println("[value of short] : "+ s);
        System.out.println("[value of int] : " +i);
        System.out.println("[value of long] : "+l);
        System.out.println("[value of float] : "+ f);
        System.out.println("[value of double] : " +d);
        System.out.println("[value of boolean] : "+bb);
        System.out.println("[value of char] : "+ch);

    }
}
